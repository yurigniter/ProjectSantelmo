	This website will keep a record of relevant information about the fire incident
	based on the input of the control room personnel, for a more reliable, organized and 
	accessible data. <br>
	The system aims to automate the process of notifying the fire
	station in any occurrence of fire alert, which will lessen factors
	affecting the response time of the firefighters to a certain fire scene
	and to organize the information about fire outbreaks efficiently.

